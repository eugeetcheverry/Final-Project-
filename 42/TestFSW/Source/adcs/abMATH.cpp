// *****************************************************************************
// * Función quaterniontomatrix
// *****************************************************************************
void quaterniontomatrix(double q0, double q1, double q2, double q3, double C[4][4])
{
  double q00=q0*q0,q11=q1*q1,q22=q2*q2,q33=q3*q3;
  C[1][1] = q11-q22-q33+q00;
  C[2][2] =-q11+q22-q33+q00;
  C[3][3] =-q11-q22+q33+q00;
  double q12=q1*q2,q30=q3*q0,q13=q1*q3,q20=q2*q0,q23=q2*q3,q10=q1*q0;
  C[1][2] = 2.0*(q12+q30);
  C[2][1] = 2.0*(q12-q30);
  C[1][3] = 2.0*(q13-q20);
  C[3][1] = 2.0*(q13+q20);
  C[2][3] = 2.0*(q23+q10);
  C[3][2] = 2.0*(q23-q10);

}


// *****************************************************************************
// * Función matrixtoquaternion
// *****************************************************************************
void matrixtoquaternion(double C[4][4], double *pq0, double *pq1, double *pq2, double *pq3)
{
  
  double trace=C[1][1]+C[2][2]+C[3][3];
  int i,maxI=0,sign=1;
  double qq0=0.,qq1=0.,qq2=0.,qq3=0.;
  if (1+trace>0.0) qq0 = 0.5*sqrt(1+trace);
  if (2*C[1][1]+1-trace>0.0) qq1 = 0.5*sqrt(2*C[1][1]+1-trace);
  if (2*C[2][2]+1-trace>0.0) qq2 = 0.5*sqrt(2*C[2][2]+1-trace);
  if (2*C[3][3]+1-trace>0.0) qq3 = 0.5*sqrt(2*C[3][3]+1-trace);
  double vqd[4]={qq0*qq0,qq1*qq1,qq2*qq2,qq3*qq3};
  double temp=0.;
  for (i=0;i<=3;i++) if (vqd[i]>temp) {temp=vqd[i];maxI=i;}
  switch (maxI) {
	 case 0:
        if (*pq0<0) sign=-1;
		*pq0 = 0.5*sign*sqrt(1+trace);
		if (*pq0==0.) *pq0=1.;
		*pq1 = 0.25*(C[2][3]-C[3][2])/(*pq0);
		*pq2 = 0.25*(C[3][1]-C[1][3])/(*pq0);
		*pq3 = 0.25*(C[1][2]-C[2][1])/(*pq0);
		break;
	 case 1:
        if (*pq1<0) sign=-1;
		*pq1 = 0.5*sign*sqrt(2*C[1][1]+1-trace);
		if (*pq1==0.) *pq1=1.;
		*pq2 = 0.25*(C[1][2]+C[2][1])/(*pq1);
		*pq3 = 0.25*(C[1][3]+C[3][1])/(*pq1);
		*pq0 = 0.25*(C[2][3]-C[3][2])/(*pq1);
		break;
	 case 2:
        if (*pq2<0) sign=-1;
		*pq2 = 0.5*sign*sqrt(2*C[2][2]+1-trace);
		if (*pq2==0.) *pq2=1.;
		*pq1 = 0.25*(C[1][2]+C[2][1])/(*pq2);
		*pq3 = 0.25*(C[3][2]+C[2][3])/(*pq2);
		*pq0 = 0.25*(C[3][1]-C[1][3])/(*pq2);
		break;
	 case 3:
        if (*pq3<0) sign=-1;
		*pq3 = 0.5*sign*sqrt(2*C[3][3]+1-trace);
		if (*pq3==0.) *pq3=1.;
		*pq1 = 0.25*(C[1][3]+C[3][1])/(*pq3);
		*pq2 = 0.25*(C[3][2]+C[2][3])/(*pq3);
		*pq0 = 0.25*(C[1][2]-C[2][1])/(*pq3);
		break;
  }
  temp=sqrt((*pq0)*(*pq0)+(*pq1)*(*pq1)+(*pq2)*(*pq2)+(*pq3)*(*pq3));
  if (temp==0.) temp=1.;
  *pq0/=temp;*pq1/=temp;*pq2/=temp;*pq3/=temp;
}


