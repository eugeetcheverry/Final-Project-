  MMt[2][1]=M[1][1]*M[2][1]+M[1][2]*M[2][2]+M[1][3]*M[2][3];
  MMt[2][2]=M[2][1]*M[2][1]+M[2][2]*M[2][2]+M[2][3]*M[2][3];

  double iMMt[3][3];
