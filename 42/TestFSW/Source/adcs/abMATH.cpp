// *****************************************************************************
// * Función q_mult
// *****************************************************************************
void q_mult(double q0, double q1, double q2, double q3, double q0prima, double q1prima, double q2prima, double q3prima, double *q0prima2, double *q1prima2, double *q2prima2, double *q3prima2)
{

  *q0prima2 = (- q1*q1prima - q2*q2prima - q3*q3prima + q0*q0prima);
  *q1prima2 = (q1*q0prima + q2*q3prima - q3*q2prima + q0*q1prima);
  *q2prima2 = (- q1*q3prima + q2*q0prima + q3*q1prima + q0*q2prima);
  *q3prima2 = (q1*q2prima - q2*q1prima + q3*q0prima + q0*q3prima);

  double nqtot = sqrt((*q0prima2)*(*q0prima2)+(*q1prima2)*(*q1prima2)+(*q2prima2)*(*q2prima2)+(*q3prima2)*(*q3prima2));

  if (nqtot==0.) nqtot=1.;

  *q0prima2 /= nqtot;
  *q1prima2 /= nqtot;
  *q2prima2 /= nqtot;
  *q3prima2 /= nqtot;

}


// *****************************************************************************
// * Función q_a_rad
// *****************************************************************************
void q_a_rad(double q0, double q1, double q2, double q3, double *protacion, double *pascension, double *pdeclinacion)
{

  //double Zm0,Zm1,Zm2,pi=3.1416,q0_s;
  double Zm0,Zm1,Zm2,q0_s;

  // Normalización por precisión
  double nqin=sqrt(q0*q0+q1*q1+q2*q2+q3*q3);
  if (nqin==0.) nqin=1.;
  q0/=nqin;q1/=nqin;q2/=nqin;q3/=nqin;

  // Eje X final en la terna inicial
  Zm0 = q1*q1-q2*q2-q3*q3+q0*q0;
  Zm1 = 2.0*(q1*q2+q3*q0);
  Zm2 = 2.0*(q1*q3-q2*q0);

  // Ascención
  if (Zm0==0.) {
    if (Zm1>0.) *pascension = pi/2.0;
    if (Zm1<0.) *pascension = -pi/2.0;
    if (Zm1==0.) *pascension = 0.;
  } else {
    *pascension = atan2(Zm1, Zm0);
  }

  // Declinación
  double nZm01 = sqrt(Zm0*Zm0+Zm1*Zm1);
  if (nZm01==0.) {
    if (Zm2>0.) *pdeclinacion = pi/2.0;
    if (Zm2<0.) *pdeclinacion = -pi/2.0;
    if (Zm2==0.) *pdeclinacion = 0.;
  } else {
    *pdeclinacion = atan(Zm2/nZm01); // Cambio: basta con atan
  }

  // Rotación
  double nZm12 = sqrt(Zm1*Zm1+Zm2*Zm2);
  if (nZm12==0.) {
    if (q0>1.0) q0=1.0;
    if (q0<-1.0) q0=-1.0;
    *protacion=2.0*acos(q0);
    if (q1<0.) *protacion=-*protacion;
  } else {
    q0_s=-sin(0.5*acos(Zm0))*(q2*Zm2-q3*Zm1)/nZm12+q0*cos(0.5*acos(Zm0));
    if (q0_s>1.0) q0_s=1.0;
    if (q0_s<-1.0) q0_s=-1.0;
    *protacion=2.0*acos(q0_s);
    if (q1<0.) *protacion=-*protacion;
  }

}


// *****************************************************************************
// * Función rad_a_q
// *****************************************************************************
void rad_a_q(double *pq0, double *pq1, double *pq2, double *pq3, double rotacion, double ascension, double declinacion)
{

  double q0asc, q1asc, q2asc, q3asc, q0dec, q1dec, q2dec, q3dec, q0rot, q1rot, q2rot, q3rot;
  double q0dec_rot, q1dec_rot, q2dec_rot, q3dec_rot, q0tot, q1tot, q2tot, q3tot;

  // Quaternión de ascención
  q0asc = cos(ascension/2.0);
  q1asc = 0.;
  q2asc = 0.;
  q3asc = sin(ascension/2.0);

  // Quaternión de declinación
  q0dec = cos(declinacion/2.0);
  q1dec = sin(declinacion/2.0)*sin(ascension);
  q2dec = -sin(declinacion/2.0)*cos(ascension);
  q3dec = 0.;

  // Quaternión de rotación
  q0rot = cos(rotacion/2.0);
  q1rot = sin(rotacion/2.0)*cos(ascension)*cos(declinacion);
  q2rot = sin(rotacion/2.0)*sin(ascension)*cos(declinacion);
  q3rot = sin(rotacion/2.0)*sin(declinacion);

  // Composición de cuaterniones
  q_mult(q0dec, q1dec, q2dec, q3dec, q0rot, q1rot, q2rot, q3rot, &q0dec_rot, &q1dec_rot, &q2dec_rot, &q3dec_rot);
  q_mult(q0asc, q1asc, q2asc, q3asc, q0dec_rot, q1dec_rot, q2dec_rot, q3dec_rot, &q0tot, &q1tot, &q2tot, &q3tot);

  // Resultado
  *pq0 = q0tot;
  *pq1 = q1tot;
  *pq2 = q2tot;
  *pq3 = q3tot;

}


// *****************************************************************************
// * getCd
// *****************************************************************************
void getCd(double vpoint[4], double Cd[4][4])
{

  double vx[4] = {0,1,0,0};
  double va[4], vb[4];

  pVec(vpoint,vx,vb);
  pVec(vb,vpoint,va);

  double nn=sqrt(va[1]*va[1]+va[2]*va[2]+va[3]*va[3]);
  if (nn==0.) nn=1.;
  va[1] = va[1]/nn;
  va[2] = va[2]/nn;
  va[3] = va[3]/nn;

  nn=sqrt(vb[1]*vb[1]+vb[2]*vb[2]+vb[3]*vb[3]);
  if (nn==0.) nn=1.;
  vb[1] = vb[1]/nn;
  vb[2] = vb[2]/nn;
  vb[3] = vb[3]/nn;

  Cd[1][1]=va[1];
  Cd[1][2]=va[2];
  Cd[1][3]=va[3];
  Cd[2][1]=vb[1];
  Cd[2][2]=vb[2];
  Cd[2][3]=vb[3];
  Cd[3][1]=vpoint[1];
  Cd[3][2]=vpoint[2];
  Cd[3][3]=vpoint[3];

}


// *****************************************************************************
// * pVec
// *****************************************************************************
void pVec(double v1[], double v2[], double v3[])
{
  v3[1] = v1[2]*v2[3] - v1[3]*v2[2];
  v3[2] = v1[3]*v2[1] - v1[1]*v2[3];
  v3[3] = v1[1]*v2[2] - v1[2]*v2[1];
}
