void rotation(double C[4][4],double vin[4],double vout[4])
{
  int i,j;
  for (i=1;i<=3;i++) for (j=1;j<=3;j++) vout[i]=C[i][j]*vin[j];
}


// *****************************************************************************
// * Función triad para sigma(v1) < sigma(v2) en ternas (t)eórica y (m)edida
// *****************************************************************************
void triad(double v1m[4], double v2m[4], double v1t[4], double v2t[4], double Cmt[4][4])
{
  double n2m[4], n3m[4], n2t[4], n3t[4];
  int i,j;

  normalize(v1m);
  vectorialproduct(v1m,v2m,n2m);normalize(n2m);
  vectorialproduct(v1m,n2m,n3m);
  vectorialproduct(v1t,v2t,n2t);normalize(n2t);
  vectorialproduct(v1t,n2t,n3t);

  for(i=1;i<=3;i++) for(j=1;j<=3;j++) Cmt[i][j]=v1m[i]*v1t[j]+n2m[i]*n2t[j]+n3m[i]*n3t[j];
}


// *****************************************************************************
// * Función vectorialproduct
// *****************************************************************************
void vectorialproduct(double v1[4],double v2[4],double r[4])
{
  r[1]=v1[2]*v2[3]-v1[3]*v2[2];
  r[2]=v1[3]*v2[1]-v1[1]*v2[3];
  r[3]=v1[1]*v2[2]-v1[2]*v2[1];
}


// *****************************************************************************
// * Función normalize
// *****************************************************************************
void normalize(double v[4])
{
  double n=sqrt(v[1]*v[1]+v[2]*v[2]+v[3]*v[3]);
  if (n>0.) {
	v[1]/=n;
    v[2]/=n;
    v[3]/=n;
  } else {
	printf("\nEnormalize");
  }
} 


